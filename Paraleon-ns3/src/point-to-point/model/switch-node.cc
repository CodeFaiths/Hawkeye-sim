void SwitchNode::ChangeECNthreshold(std::map<std::string, std::string> parameter_map)
{
	int kmin = 400, kmax = 1600;
	double pmax = 0.2;
	for (auto it : parameter_map)
	{
		std::string parameter_key_str = it.first;
		std::string parameter_value_str = it.second;
		if (parameter_key_str == "kmin" || parameter_key_str == "kmax")
		{
			int parameter_value_int = std::stoi(parameter_value_str);
			if (parameter_key_str == "kmin")
				kmin = parameter_value_int;
			else if (parameter_key_str == "kmax")
				kmax = parameter_value_int;
			else if (parameter_key_str == "pmax")
				pmax = parameter_value_int;
		}
		else if (parameter_key_str == "pmax")
		{
			pmax = std::stof(parameter_value_str);
		}
	}

	for (int i = 0; i < GetNDevices(); i++)
		m_mmu->ConfigEcn(i, kmin, kmax, pmax);
}

