// This function can only be called in switch mode
bool SwitchNode::SwitchReceiveFromDevice(Ptr<NetDevice> device, Ptr<Packet> packet, CustomHeader &ch){
	FlowIdTag t;
	Ptr<Packet> p_temp = packet->Copy();
	p_temp->PeekPacketTag(t);
	uint32_t inDev = t.GetFlowId();
	
	if (ch.l3Prot == 0x11)
		m_rxBytes[inDev] += p_temp->GetSize();

	PppHeader ppp;
	Ipv4Header h;
	packet->RemoveHeader(ppp);
	packet->RemoveHeader(h);
	uint8_t current_ttl = h.GetTtl();

	if (add_to_sketch && current_ttl <= 64 && ch.l3Prot != 0xFC)
	{
		uint32_t flow_id = GetFlowId(packet, ch);
		elastic->insert(reinterpret_cast<uint8_t*>(&flow_id), sizeof(flow_id));
		h.SetTtl(150);
	}

	packet->AddHeader(h);
	packet->AddHeader(ppp);
	
	SendToDev(packet, ch);
	return true;
}

